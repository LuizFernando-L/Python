a = input("Digite Algo?")
print("O tipo desse valor é ", type(a)) #Testando o tipo da váriavel
print("Só tem espaços: ", a.isspace()) #Testando se só possui espaços
print("È um número: ", a.isnumeric()) #Testando se é numérica
print("É alfabética: ", a.isalpha()) #Testando se é alfabética
print("É alfha numérica: ", a.isalnum()) #Testando se é alfanumérica
print("É maiúscula? ", a.isupper()) #testando se é maiúscula
print("É minúscula? ", a.islower()) #Testando se é minúscula

